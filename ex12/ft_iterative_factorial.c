int		ft_iterative_factorial(int nb)
{
	int x;

	x = nb;
	if (nb == 0)
		return (1);
	if (nb <= 12)
	{
		if (nb > 0)
		{
			while (nb > 1)
			{
				x *= --nb;
			}
			return (x);
		}
	}
	return (0);
}
